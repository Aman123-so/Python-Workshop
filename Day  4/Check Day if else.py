#  if , elif , else case 
x=int(input("enter a number 1 - 7: "))
if x==1:
    print("Sunday")
elif x==2:
    print("Monday")
elif x==3:
    print("Tuesday")
elif x==4:
    print("wednesday")
elif x==5:
    print("thursday")
elif x==6:
    print("friday")
elif x==7:
    print("Saturday")
else:
    print("sorry pls enter no. between 1 to 7 ")
