#Area of trapezium
a = int(input("Enter the value a: "))  # Length of one parallel side (base 1)
b = int(input("Enter the value b: "))  # Length of the other parallel side (base 2)
h = int(input("Enter the value h: "))  # Height (perpendicular distance between the two parallel sides)
area = ((a+b)/2)*h
print("Area is:", area)
