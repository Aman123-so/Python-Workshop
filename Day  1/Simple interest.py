# Simple interest 
p=int(input("Enter the value of p:")) #  Principal (The initial amount of money invested or borrowed)
r=int(input("Enter the value of r:")) #  Rate of Interest (Annual interest rate in percentage)
t=int(input("Enter the value of t:")) #  Time (The period for  the money, usually in years)
si=(p*r*t)/100
print("Simple interest  is: ",si)



